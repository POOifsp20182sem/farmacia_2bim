package br.ifsp.poo.farmacia.modelo.entidade;

public enum EnumFormaFarmaco {
	COMPRIMIDO("comprimido"), CAPSULA("capsula"), DRAGEA("dragea"), PILULA("pilula"), SOLUCAO("solucao"),
	SUSPENSAO("suspensao"), EMULSAO("emulsao"), OVULO("ovulo"), POMADA("pomada"), SUPOSITORIO("supositorio"),
	LINIMENTO("linimento");

	private String formaFarmaco;

	private EnumFormaFarmaco(String formaFarmaco) {
		this.formaFarmaco = formaFarmaco;
	}

	public String getFormaFarmaco() {
		return formaFarmaco;
	}

}
